UnaryTab: ;; [tab(]	

		jsr 	EXPEvalInteger8 			; count of spaces.
		pha
		jsr 	ERRCheckRParen
		pla
		sta 	ControlParameters 			; set parameter #0
		.DoSendMessage 						; set tab position.
		.byte 	2,17
		.DoWaitMessage		
		lda 	#0
		jsr 	StringTempAllocate 			; allocate string space.
		rts

		.send code

