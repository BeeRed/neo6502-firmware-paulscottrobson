;					ERL Returns line number, ERR returns error code
;
; ************************************************************************************************

		.section code	

EXPUnaryErl: ;; [erl]
		lda 	errLineNumber
		sta 	XSNumber0,x
		lda 	errLineNumber+1
		sta 	XSNumber1,x
EXPEUEOut:		
		stz 	XSNumber2,x
		stz 	XSNumber3,x
		stz 	XSControl,x
		rts

EXPUnaryErr: ;; [err]
		lda 	errNumber
		sta 	XSNumber0,x
		stz 	XSNumber1,x
		bra 	EXPEUEOut

		.send code


