EXPUnaryHaveMouse: ;; [havemouse(]
		jsr 	ERRCheckRParen 					; )

		.DoSendMessage 							; read the mouse flag
		.byte 	11,4
		.DoWaitMessage

		lda 	ControlParameters 				; returned value
	 	jmp 	EXPUnaryReturnA

		.send code
				
