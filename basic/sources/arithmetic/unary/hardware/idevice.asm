UnaryIDevice: ;; [idevice(]	
		jsr 	EXPEvalInteger8				; get device to check.
		sta 	ControlParameters 			; try to read this device.
		jsr 	ERRCheckRParen 				

		.DoSendMessage 						; scan for it
		.byte 	10,8
		.DoWaitMessage
		lda 	ControlParameters  			; return 0 / 1
		jmp 	ReturnBoolean

