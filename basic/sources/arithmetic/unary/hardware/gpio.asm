;
; ************************************************************************************************

UnaryPin: ;; [pin(]	
		jsr 	EXPEvalInteger8				; get pin to check
		pha
		jsr 	ERRCheckRParen 				
		pla
		sta 	ControlParameters
		.DoSendMessage 						; read it.
		.byte 	10,3
		.DoWaitMessage
		lda 	ControlError
		bne 	_UPRange
		lda 	ControlParameters  			; return 0 / 1
		beq 	_UPZero
		lda 	#1
_UPZero:		
		jmp 	EXPUnaryReturnA
_UPRange:
		.error_range

		.send code

