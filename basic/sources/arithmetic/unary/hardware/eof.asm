		.section code	

EXPUnaryEof: ;; [eof(]
		jsr 	EXPEvalInteger8 				; file handle
		pha
		jsr 	ERRCheckRParen 					; )
		pla
		sta 	ControlParameters+0

		.DoSendMessage 							; do file stat
		.byte 	3,22
		.DoWaitMessage				

		lda 	ControlError 					; error ?
		bne 	_EUEError
		lda 	ControlParameters+0		
		jmp 	EXPUnaryReturnA
_EUEError:
		.error_file

		.send code


