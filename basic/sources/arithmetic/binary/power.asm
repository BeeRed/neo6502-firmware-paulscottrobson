BinaryPower: ;; [pow(]
		jsr 	EXPEvalNumber 				; one operand
		jsr 	ERRCheckComma
		inx		
		jsr 	EXPEvalNumber 				; second operand
		dex
		jsr 	ERRCheckRParen

		lda 	#7
		jsr 	DoMathCommand
		rts

		.send code
				

