		.section code

Command_ON:	;; [on]
		lda 	(codePtr),y 				; check for ERROR
		cmp 	#KWD_SYS_SH1
		bne 	_CONSyntax
		iny
		lda 	(codePtr),y
		cmp 	#KWD_ERROR-$100
		bne 	_CONSyntax
		iny

		sty 	errCode+2 					; save error handler position
		lda 	codePtr
		sta 	errCode
		lda 	codePtr+1
		sta 	errCode+1
		jmp 	RUNEndOfLine 				; ignore the rest of the line

_CONSyntax:
		.error_syntax

		.send code
					
