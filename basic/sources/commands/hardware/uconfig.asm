		.section code

Command_UCONFIG:	;; [uconfig]
		ldx 	#0
		jsr 	EXPEvalInteger16 			; baud rate
		lda 	XSNumber0
		sta 	ControlParameters+0
		lda 	XSNumber1
		sta 	ControlParameters+1
		stz 	ControlParameters+2		
		.DoSendMessage 						; configure UART
		.byte 	10,15
		rts		

		.send code

