;									Set line #, in buffer.
;
; ************************************************************************************************

		.section code

EDPutLine:
		lda 	ControlParameters+1 		; push line # on stack
		pha
		lda 	ControlParameters+2
		pha
		jsr 	TOKTokenise 				; tokenise the line
		pla  								; set up the line # to update.
		sta 	TOKLineNumber+1
		pla
		sta 	TOKLineNumber

		jsr 	PGMDeleteLine 				; delete line specified
		jsr 	PGMInsertLine 				; insert line.
		rts

		.send code

				
